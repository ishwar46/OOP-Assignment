package Practice;

public class WhileLoops {
    public static void main(String[] args) {
        System.out.println("While Loop");
        int i = 1;
        while (i<=3) {
            System.out.println(i);
            i++;
        }
        System.out.println("Loop Ends");
        while (true){
            System.out.println("Having Fun!");
        }
    }
}
